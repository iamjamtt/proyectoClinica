package Entidad;

public class especialidad {
    int ide;
    String nombree;

    public especialidad() {
    }

    public especialidad(int ide, String nombree) {
        this.ide = ide;
        this.nombree = nombree;
    }

    public int getIde() {
        return ide;
    }

    public void setIde(int ide) {
        this.ide = ide;
    }

    public String getNombree() {
        return nombree;
    }

    public void setNombree(String nombree) {
        this.nombree = nombree;
    }
    
    
}
